using UnityEngine;

public class LightColorChangerForClear2 : MonoBehaviour
{
    private Light pointLight;
    private float timer = 0f;
    private float changeInterval = 3f; // 3秒ごとに切り替える

    void Start()
    {
        pointLight = GetComponent<Light>();
        if (pointLight == null)
        {
            Debug.LogError("Light component not found on this GameObject.");
        }
        else
        {
            pointLight.color = Color.red; // デフォルトの色を赤に設定
        }
    }

    void Update()
    {
        // 3秒ごとにisClear2を切り替える
        timer += Time.deltaTime;
        if (timer >= changeInterval)
        {
            GlobalVariables.IsClear2 = !GlobalVariables.IsClear2;
            timer = 0f;
        }

        // isClear2の値に応じてポイントライトの色を変更
        if (GlobalVariables.IsClear2)
        {
            pointLight.color = new Color(0.31f, 0.78f, 0.47f); // エメラルドグリーンのRGB値
        }
        else
        {
            pointLight.color = Color.red; // デフォルトの色（赤）
        }
    }
}
